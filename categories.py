def normalize_text(text: str) -> str:
    if not text:
        return ""
    text = text.lower().strip()
    text = re.sub(r"\s+", " ", text)
    return text


def extract_words(text: str) -> List[str]:
    normalized = normalize_text(text)
    words = normalized.split()
    return [w for w in words if len(w) >= 2]


def find_category_by_keyword(word: str) -> Optional[str]:
    word = normalize_text(word)
    if len(word) < 2:
        return None

    for category, keywords in KEYWORDS.items():
        if category == "other":
            continue
        for keyword in keywords:
            if keyword == word:
                return category
    return None


def find_category_by_partial_match(word: str) -> Optional[str]:
    word = normalize_text(word)
    if len(word) < 2:
        return None

    for category, keywords in KEYWORDS.items():
        if category == "other":
            continue
        for keyword in keywords:
            if keyword in word or word in keyword:
                return category
    return None


def find_category_by_fuzzy_match(word: str, threshold: float = 0.7) -> Optional[str]:
    word = normalize_text(word)
    if len(word) < 3:
        return None

    all_keywords = []
    for category, keywords in KEYWORDS.items():
        if category == "other":
            continue
        for keyword in keywords:
            all_keywords.append((category, keyword))

    keyword_list = [kw for _, kw in all_keywords]
    matches = get_close_matches(word, keyword_list, n=1, cutoff=threshold)

    if matches:
        for category, keyword in all_keywords:
            if keyword == matches[0]:
                return category

    return None


def categorize_product(
    name: str, user_categories: Optional[Dict[str, str]] = None
) -> str:
    if not name:
        return "other"

    normalized_name = normalize_text(name)

    if user_categories:
        for saved_name, category in user_categories.items():
            if normalize_text(saved_name) == normalized_name:
                return category
            if normalize_text(saved_name) in normalized_name:
                return category
            if normalized_name in normalize_text(saved_name):
                return category

    words = extract_words(name)

    for word in words:
        category = find_category_by_keyword(word)
        if category:
            return category

    for word in words:
        category = find_category_by_partial_match(word)
        if category:
            return category

    for word in words:
        if len(word) >= 3:
            category = find_category_by_fuzzy_match(word)
            if category:
                return category

    return "other"


def get_category_name(category_id: str) -> str:
    if category_id in CATEGORIES:
        return CATEGORIES[category_id]["name"]
    return CATEGORIES["other"]["name"]


def get_category_emoji(category_id: str) -> str:
    name = get_category_name(category_id)
    emoji = name.split()[0]
    return emoji


def get_sorted_categories() -> List[str]:
    return sorted(CATEGORIES.keys(), key=lambda x: CATEGORIES[x]["order"])


def get_all_categories() -> Dict[str, Dict[str, any]]:
    return CATEGORIES.copy()
